package Java_Basics;
public class Printf {
    public static void main(String[] args){
        boolean b = true;
        int ans = 56;
        double al = 5.6;
        System.out.printf("%b" , b);
        System.out.println();
        System.out.printf("%d" , ans);
        System.out.println();
        System.out.printf("%f" , al);
        System.out.println();

    }
}
